package front_java;

public class Locadoras {
	int codigo;
	String nome;
	String ano;
	
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getAno() {
		return ano;
	}
	public void setAno(String uf) {
		this.ano = uf;
	}
	
	
}
